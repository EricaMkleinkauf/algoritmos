const variavel1 =50
const variavel2 = 35 

//igual; ===

console.log(variavel1 === (2*25)) //true
console.log(variavel1 === variavel2) //false

// diferente; !==
console.log(variavel1 !== variavel2)

//Maior >
// verifica se o valor da esquerda é maior que o da direita 
console.log(variavel1 > variavel2)
console.log(variavel2 > variavel1)


//Maior ou igual; e >=
//verifica se o valor da esqueda é maior ou igual ao valor da direita
console.log(variavel1 >= variavel2) //true
console.log(variavel2 >= variavel1) //false 

//Menor; <
//verifica se o valor da esquerda e menor que o valor da direita
console.log(variavel1 < variavel2) //false 
console.log(23 < 90) //true

//mMenor ou igual; <= 
//verifica se oo valor da esquerda é menor ou igual ao da direita
console.log(variavel1 <= variavel2)//false
console.log(variavel1 <= variavel2) //true